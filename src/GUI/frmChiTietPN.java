     public void loadCTPN() {
        try {
            DefaultTableModel model = new DefaultTableModel();
            model.setColumnIdentifiers(new String[]{"Tên sản phẩm","Màu sắc","Kích cỡ", "Số lượng nhập", "Giá nhập"});
            chiTietPhieuNhapDao ctpnDAO = new chiTietPhieuNhapDao();
            ArrayList<chiTietPhieuNhapPojo> dsCTPN = ctpnDAO.layDSCTPN(frmNhanVienKho.maPN);
            for (chiTietPhieuNhapPojo ct : dsCTPN) {
                String tenSP = ct.getTensp();
                String mau = ct.getMauSac();
                String kt = ct.getKichThuoc();
                String giaNhap = ct.getGianhap();
                String soLuong = ct.getSoluong();
                model.addRow(new Object[]{tenSP,mau,kt, soLuong, formatter.format(Double.parseDouble(giaNhap)) + " VNĐ"});
            }

            tbCTPN.setModel(model);
            lbMPN.setText(frmNhanVienKho.maPN);
            lbNCC.setText(frmNhanVienKho.nhaCungCap);
            lbNV.setText(frmNhanVienKho.nhanVienLap);
            lbTG.setText(frmNhanVienKho.ngayLap);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }


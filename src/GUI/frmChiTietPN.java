    private void button1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_button1MouseClicked
        String url = "";
        JFileChooser j = new JFileChooser();
        j.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        int x = j.showSaveDialog(this);
        if (x == JFileChooser.APPROVE_OPTION) {
            url = j.getSelectedFile().getPath() + File.separator;
        }

        String fileName = "phieunhap.pdf";
        File file = new File(url + fileName);

        int counter = 1;
        while (file.exists()) {
            fileName = "phieunhap(" + counter + ").pdf";
            file = new File(url + fileName);
            counter++;
        }

        Document doc = new Document();
        try {
            PdfWriter.getInstance(doc, new FileOutputStream(file));
            doc.open();

            BaseFont bf = BaseFont.createFont("Roboto/Roboto-Regular.ttf", BaseFont.IDENTITY_H, BaseFont.EMBEDDED);
            Font font = new Font(bf, 12);
            Font titleFont = new Font(bf, 18, Font.BOLD); 
           
            PdfPTable headerTable1 = new PdfPTable(1);
            headerTable1.setWidthPercentage(100);
            headerTable1.setSpacingAfter(15);

            PdfPCell cellTT1 = new PdfPCell(new Phrase("Công ty TNHH Thời Trang và Mỹ Phẩm Châu Âu", font));
            cellTT1.setBorder(Rectangle.NO_BORDER);
            PdfPCell cellTT2 = new PdfPCell(new Phrase("Địa chỉ: Lầu 12, Toà Nhà Sonatus, 15 Lê Thánh Tôn, Phường Bến Nghé, Quận 1, TP.HCM", font));
            cellTT2.setBorder(Rectangle.NO_BORDER);
            PdfPCell cellTT3 = new PdfPCell(new Phrase("Điện thoại: +84(28) 3825 6523", font));
            cellTT3.setBorder(Rectangle.NO_BORDER);

            headerTable1.addCell(cellTT1);
            headerTable1.addCell(cellTT2);
            headerTable1.addCell(cellTT3);
            doc.add(headerTable1);
            
            Paragraph title = new Paragraph("PHIẾU NHẬP", titleFont);
            title.setAlignment(Element.ALIGN_CENTER);
            title.setSpacingAfter(20);
            doc.add(title);

            PdfPTable headerTable = new PdfPTable(2);
            headerTable.setWidthPercentage(100);
            headerTable.setSpacingAfter(10);

            headerTable.setWidths(new float[]{1, 1});

            PdfPCell cell1 = new PdfPCell(new Phrase("Mã phiếu nhập: " + lbMPN.getText(), font));
            cell1.setBorder(Rectangle.NO_BORDER);
            PdfPCell cell2 = new PdfPCell(new Phrase("Nhà cung cấp: " + lbNCC.getText(), font));
            cell2.setBorder(Rectangle.NO_BORDER);

            PdfPCell cell3 = new PdfPCell(new Phrase("Nhân viên lập: " + lbNV.getText(), font));
            cell3.setBorder(Rectangle.NO_BORDER);
            PdfPCell cell4 = new PdfPCell(new Phrase("Thời gian lập: " + lbTG.getText(), font));
            cell4.setBorder(Rectangle.NO_BORDER);

            headerTable.addCell(cell1);
            headerTable.addCell(cell2);
            headerTable.addCell(cell3);
            headerTable.addCell(cell4);

            doc.add(headerTable);

            PdfPTable tbl = new PdfPTable(7);
            tbl.setWidthPercentage(100);
            tbl.setWidths(new float[]{0.5f, 2f, 1.2f, 1.2f, 1.8f, 1.5f, 2f});
            
            PdfPCell header1 = new PdfPCell(new Phrase("STT", font));
            header1.setHorizontalAlignment(Element.ALIGN_CENTER);
            PdfPCell header2 = new PdfPCell(new Phrase("Tên sản phẩm", font));
            header2.setHorizontalAlignment(Element.ALIGN_CENTER);
            PdfPCell header3 = new PdfPCell(new Phrase("Màu sắc", font));
            header3.setHorizontalAlignment(Element.ALIGN_CENTER);
            PdfPCell header4 = new PdfPCell(new Phrase("Kích cỡ", font));
            header4.setHorizontalAlignment(Element.ALIGN_CENTER);
            PdfPCell header5 = new PdfPCell(new Phrase("Số lượng nhập", font));
            header5.setHorizontalAlignment(Element.ALIGN_CENTER);
            PdfPCell header6 = new PdfPCell(new Phrase("Giá nhập", font));
            header6.setHorizontalAlignment(Element.ALIGN_CENTER);
            PdfPCell header7 = new PdfPCell(new Phrase("Thành tiền", font));
            header7.setHorizontalAlignment(Element.ALIGN_CENTER);
            tbl.addCell(header1);
            tbl.addCell(header2);
            tbl.addCell(header3);
            tbl.addCell(header4);
            tbl.addCell(header5);
            tbl.addCell(header6);
            tbl.addCell(header7);

            double tongTien = 0;
            int tongSLN = 0;
            int stt = 1;
            for (int i = 0; i < tbCTPN.getRowCount(); i++) {
                String tenSanPham = tbCTPN.getValueAt(i, 0).toString();
                String mau = tbCTPN.getValueAt(i, 1).toString();
                String kt = tbCTPN.getValueAt(i, 2).toString();
                String soLuongNhap = tbCTPN.getValueAt(i, 3).toString();
                String giaNhap = tbCTPN.getValueAt(i, 4).toString();
                String giaNhapClean = giaNhap.replaceAll("[^\\d]", "");

                double giaNhapDouble = Double.parseDouble(giaNhapClean);

                tongTien += giaNhapDouble * Integer.parseInt(soLuongNhap);
                tongSLN += Integer.parseInt(soLuongNhap);
                double thanhTien = giaNhapDouble * Integer.parseInt(soLuongNhap);
               
                
                PdfPCell cellSTT = new PdfPCell(new Phrase(String.valueOf(stt), font));
                cellSTT.setHorizontalAlignment(Element.ALIGN_CENTER);
                tbl.addCell(cellSTT);
                
                PdfPCell cellTenSP = new PdfPCell(new Phrase(tenSanPham, font));
                cellTenSP.setHorizontalAlignment(Element.ALIGN_CENTER);
                tbl.addCell(cellTenSP);
                
                PdfPCell cellMauSac = new PdfPCell(new Phrase(mau, font));
                cellMauSac.setHorizontalAlignment(Element.ALIGN_CENTER);
                tbl.addCell(cellMauSac);
                
                PdfPCell cellKT= new PdfPCell(new Phrase(kt, font));
                cellKT.setHorizontalAlignment(Element.ALIGN_CENTER);
                tbl.addCell(cellKT);
                
                PdfPCell cellSLN = new PdfPCell(new Phrase(soLuongNhap, font));
                cellSLN.setHorizontalAlignment(Element.ALIGN_RIGHT);
                tbl.addCell(cellSLN);
                
                PdfPCell cellGiaNhap = new PdfPCell(new Phrase(giaNhap, font));
                cellGiaNhap.setHorizontalAlignment(Element.ALIGN_RIGHT);
                tbl.addCell(cellGiaNhap);
                
                PdfPCell cellThanhTien = new PdfPCell(new Phrase(formatter.format(thanhTien) + " VNĐ", font));
                cellThanhTien.setHorizontalAlignment(Element.ALIGN_RIGHT);
                tbl.addCell(cellThanhTien);
                stt++;
            }

            doc.add(tbl);
            
            PdfPTable bottomTable = new PdfPTable(2);
            bottomTable.setWidthPercentage(100);
            bottomTable.setSpacingBefore(20);

            bottomTable.setWidths(new float[]{1.9f, 1});

            PdfPCell cellTSL = new PdfPCell(new Phrase("Tổng số lượng nhập: " + tongSLN , font));
            cellTSL.setHorizontalAlignment(Element.ALIGN_RIGHT);
            cellTSL.setBorder(Rectangle.NO_BORDER);
            PdfPCell cellTT = new PdfPCell(new Phrase("Tổng tiền: " + formatter.format(tongTien) + " VNĐ" , font));
            cellTT.setHorizontalAlignment(Element.ALIGN_RIGHT);
            cellTT.setBorder(Rectangle.NO_BORDER);

            bottomTable.addCell(cellTSL);
            bottomTable.addCell(cellTT);

            doc.add(bottomTable);
            
            JOptionPane.showMessageDialog(rootPane, "Xuất phiếu nhập thành công !");
        } catch (Exception e) {
            e.printStackTrace();
        } finally {
            doc.close();
        }

    }//GEN-LAST:event_button1MouseClicked


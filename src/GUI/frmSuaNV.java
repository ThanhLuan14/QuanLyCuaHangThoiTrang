    void loadText() {
        ResultSet rs = null;
        InputStream is = null;
        String maNV = frmAdmin.maNV;
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
        String sql = "SELECT * FROM qldata.NHANVIEN WHERE MANHANVIEN ='" + maNV + "'";
        try {
            rs = Database.excuteQuery(sql);

            if (rs.next()) {
                is = rs.getBinaryStream("HINHANHNV");
                String date = rs.getString("NgaySinh");
                String ten = rs.getString("Hoten");
                String gioitinh = rs.getString("Gioitinh");
                String sdt = rs.getString("sodienthoai");
                String email = rs.getString("email");
                String diachi = rs.getString("diachi");
                String maCV = rs.getString("machucvu");
                Date ngaySinh = dateFormat.parse(date);
                BufferedImage img = ImageIO.read(is);
                Image catHinh = img.getScaledInstance(hinhanh.getWidth(), hinhanh.getHeight(), Image.SCALE_SMOOTH);
                if(gioitinh.equals("Nam")){
                    loadNam();
                }
                else{
                    loadNu();
                }
                if(maCV.equals("CV001")){
                    loadNVTN();
                }
                else{
                    loadNVK();
                }
                hinhanh.setIcon(new ImageIcon(catHinh));
                namSinh.setDate(ngaySinh);
                txtTen.setText(ten);
                txtSdt.setText(sdt);
                txtEmail.setText(email);
                txtdiaChi.setText(diachi);
                txtMaNV.setText(frmAdmin.maNV);
            }
        } catch (Exception e) {
            this.dispose();
            JOptionPane.showMessageDialog(rootPane, "Nhân viên này chưa có hình ảnh");

        }
    }


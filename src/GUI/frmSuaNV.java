    private void panelRound2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_panelRound2MouseClicked
        String ten = txtTen.getText();
        String ngay = chuyenDoi(namSinh.getDate());
        String sdt = txtSdt.getText();
        String diaChi = txtdiaChi.getText();
        String email = txtEmail.getText();
        String gt = cboGT.getSelectedItem().toString();
        String cv = layMa(cboCV.getSelectedItem().toString());
        String ma = txtMaNV.getText();

        try {
            String sql;
            PreparedStatement ps;
            if (!kiemTraSDT(sdt)) {
                JOptionPane.showMessageDialog(rootPane, "Số điện thoại không hợp lệ");
            } else {
                if (!kiemTraEmail(email)) {
                    JOptionPane.showMessageDialog(rootPane, "Email không hợp lệ");
                } else {
                    if (!kiemTraTuoi(ngay)) {
                        JOptionPane.showMessageDialog(rootPane, "Nhân viên chưa đủ 18 tuổi");
                    } else {
                        if (fileHinhAnh != null) {
                            sql = "UPDATE qldata.NhanVien SET MaChucVu = ?, HoTen = ?, NgaySinh = to_date(?,'DD-MM-YYYY'), GioiTinh = ?, SoDienThoai = ?, Email = ?, DiaChi = ?, TrangThai = ?, HinhAnhNV = ? WHERE MaNhanVien = ?";
                            ps = conn.prepareStatement(sql);

                            try (FileInputStream fis = new FileInputStream(fileHinhAnh)) {
                                ps.setString(1, cv);
                                ps.setString(2, ten);
                                ps.setString(3, ngay);
                                ps.setString(4, gt);
                                ps.setString(5, sdt);
                                ps.setString(6, email);
                                ps.setString(7, diaChi);
                                ps.setInt(8, 1);
                                ps.setBlob(9, fis);
                                ps.setString(10, ma);
                            }
                        } else {
                            sql = "UPDATE qldata.NhanVien SET MaChucVu = ?, HoTen = ?, NgaySinh = to_date(?,'DD-MM-YYYY'), GioiTinh = ?, SoDienThoai = ?, Email = ?, DiaChi = ?, TrangThai = ? WHERE MaNhanVien = ?";
                            ps = conn.prepareStatement(sql);
                            ps.setString(1, cv);
                            ps.setString(2, ten);
                            ps.setString(3, ngay);
                            ps.setString(4, gt);
                            ps.setString(5, sdt);
                            ps.setString(6, email);
                            ps.setString(7, diaChi);
                            ps.setInt(8, 1);
                            ps.setString(9, ma);
                        }
                        ps.executeUpdate();
                        JOptionPane.showMessageDialog(this, "Cập nhật nhân viên thành công!");
                    }
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Có lỗi xảy ra khi cập nhật.");
        }
    }//GEN-LAST:event_panelRound2MouseClicked


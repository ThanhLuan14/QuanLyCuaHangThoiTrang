    public String id_NCC() {
        int id = 1;
        ArrayList<nhaCungCapPojo> listNCC = new nhaCungCapDao().layMaNCC();
        Set<String> maNCCSet = new HashSet<String>();
        for (nhaCungCapPojo ncc : listNCC) {
            String maNCC = ncc.getMancc();
            maNCCSet.add(maNCC);
        }
        while (maNCCSet.contains(String.format("NCC%03d", id)) && id <= 999) {
            id++;
        }
        return String.format("NCC%03d", id);
    }

    public static boolean kiemTraSDT(String sdt) {

        if (sdt == null || sdt.length() < 10 || sdt.length() > 11) {
            return false;
        }

        if (!sdt.matches("\\d+")) {
            return false;
        }

        String soDauVN = "03,07,08,09,02"; 
        String soDau = sdt.substring(0, 2);

        if (!soDauVN.contains(soDau)) {
            return false;
        }

        return true;
    }
    
    public static boolean kiemTraEmail(String email) {
        if (email.endsWith("@gmail.com")) {
            return true;
        }
        return false;
    }

  


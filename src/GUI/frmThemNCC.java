    private void panelRound2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_panelRound2MouseClicked
        String mancc = txtMaNCC.getText();
        String ten = txtTenNCC.getText();
        String sdt = txtSdt.getText();
        String email = txtEmail.getText();
        String diachi = txtdiaChi.getText();
        if(!kiemTraSDT(sdt)){
            JOptionPane.showMessageDialog(rootPane, "Số điện thoại không hợp lệ");
        }
        else {
            if(!kiemTraEmail(email)){
                JOptionPane.showMessageDialog(rootPane, "Email không hợp lệ");
            }
            else{
                String sql = "insert into qldata.nhacungcap values('" + mancc + "','" + ten + "','" + sdt + "','" + email + "','" + diachi + "',1)";
                if (Database.excuteUpdate(sql)) {
                    JOptionPane.showMessageDialog(rootPane, "Thêm nhà cung cấp mới thành công !");
                    Database.excuteUpdate("commit");
                    txtMaNCC.setText(id_NCC());
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Thêm nhà cung cấp mới thất bại !");
                }
            }
        }

    }//GEN-LAST:event_panelRound2MouseClicked

